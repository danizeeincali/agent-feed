/**
 * GLOBAL THREADING TEST TEARDOWN
 * 
 * Cleanup and reporting after threading validation tests
 */

const fs = require('fs');
const path = require('path');

async function globalTeardown(config) {
  console.log('🏁 Starting Global Threading Test Teardown...');
  
  try {
    // Collect test results
    const resultsDir = 'tests/test-results';
    const reportsDir = 'tests/reports';
    
    const summary = {
      teardownTime: new Date().toISOString(),
      testResults: {},
      screenshots: [],
      videos: [],
      reports: []
    };
    
    // Collect screenshots
    if (fs.existsSync('tests/screenshots')) {
      const screenshots = fs.readdirSync('tests/screenshots')
        .filter(file => file.endsWith('.png'))
        .map(file => ({
          name: file,
          path: path.join('tests/screenshots', file),
          size: fs.statSync(path.join('tests/screenshots', file)).size,
          modified: fs.statSync(path.join('tests/screenshots', file)).mtime
        }));
      
      summary.screenshots = screenshots;
      console.log(`📸 Collected ${screenshots.length} screenshots`);
    }
    
    // Collect videos
    if (fs.existsSync('tests/videos')) {
      const videos = fs.readdirSync('tests/videos')
        .filter(file => file.endsWith('.webm') || file.endsWith('.mp4'))
        .map(file => ({
          name: file,
          path: path.join('tests/videos', file),
          size: fs.statSync(path.join('tests/videos', file)).size
        }));
      
      summary.videos = videos;
      console.log(`🎥 Collected ${videos.length} videos`);
    }
    
    // Collect reports
    if (fs.existsSync(reportsDir)) {
      const reports = fs.readdirSync(reportsDir)
        .filter(file => file.endsWith('.json') || file.endsWith('.xml') || file.endsWith('.html'))
        .map(file => ({
          name: file,
          path: path.join(reportsDir, file),
          size: fs.statSync(path.join(reportsDir, file)).size
        }));
      
      summary.reports = reports;
      console.log(`📋 Collected ${reports.length} reports`);
    }
    
    // Read test results if available
    const resultsFile = path.join(reportsDir, 'threading-results.json');
    if (fs.existsSync(resultsFile)) {
      try {
        const results = JSON.parse(fs.readFileSync(resultsFile, 'utf8'));
        summary.testResults = {
          testCount: results.suites?.reduce((acc, suite) => 
            acc + (suite.tests?.length || 0), 0) || 0,
          passed: results.suites?.reduce((acc, suite) => 
            acc + (suite.tests?.filter(test => test.status === 'passed')?.length || 0), 0) || 0,
          failed: results.suites?.reduce((acc, suite) => 
            acc + (suite.tests?.filter(test => test.status === 'failed')?.length || 0), 0) || 0,
          duration: results.stats?.duration || 0
        };
        
        console.log('📊 Test Results Summary:');
        console.log(`   Total Tests: ${summary.testResults.testCount}`);
        console.log(`   Passed: ${summary.testResults.passed}`);
        console.log(`   Failed: ${summary.testResults.failed}`);
        console.log(`   Duration: ${summary.testResults.duration}ms`);
        
      } catch (error) {
        console.log('⚠️ Could not parse test results:', error.message);
      }
    }
    
    // Generate comprehensive summary report
    const summaryReport = `# Threading Validation Test Summary

**Generated:** ${summary.teardownTime}

## Test Execution Summary

- **Total Tests:** ${summary.testResults.testCount || 'Unknown'}
- **Passed:** ${summary.testResults.passed || 'Unknown'}
- **Failed:** ${summary.testResults.failed || 'Unknown'}
- **Duration:** ${summary.testResults.duration ? `${summary.testResults.duration}ms` : 'Unknown'}

## Artifacts Collected

### Screenshots (${summary.screenshots.length})
${summary.screenshots.map(s => `- **${s.name}** (${(s.size / 1024).toFixed(2)}KB)`).join('\n')}

### Videos (${summary.videos.length})
${summary.videos.map(v => `- **${v.name}** (${(v.size / 1024 / 1024).toFixed(2)}MB)`).join('\n')}

### Reports (${summary.reports.length})
${summary.reports.map(r => `- **${r.name}** (${(r.size / 1024).toFixed(2)}KB)`).join('\n')}

## Test Quality Assessment

${generateQualityAssessment(summary)}

---
*Generated by Threading Test Teardown*
`;
    
    // Write summary
    fs.writeFileSync(
      path.join(reportsDir, 'threading-test-summary.md'),
      summaryReport
    );
    
    fs.writeFileSync(
      path.join(reportsDir, 'threading-test-summary.json'),
      JSON.stringify(summary, null, 2)
    );
    
    console.log(`📋 Test summary written to ${reportsDir}/threading-test-summary.md`);
    
  } catch (error) {
    console.error('❌ Teardown error:', error.message);
  }
  
  console.log('✅ Global threading test teardown completed');
}

function generateQualityAssessment(summary) {
  const assessments = [];
  
  if (summary.testResults.testCount > 0) {
    const successRate = (summary.testResults.passed / summary.testResults.testCount) * 100;
    
    if (successRate >= 90) {
      assessments.push('🟢 **Excellent**: >90% test success rate');
    } else if (successRate >= 75) {
      assessments.push('🟡 **Good**: 75-90% test success rate');
    } else if (successRate >= 50) {
      assessments.push('🟠 **Fair**: 50-75% test success rate');
    } else {
      assessments.push('🔴 **Poor**: <50% test success rate');
    }
    
    if (summary.testResults.duration < 30000) {
      assessments.push('⚡ **Fast**: Tests completed in <30 seconds');
    } else if (summary.testResults.duration < 60000) {
      assessments.push('⏱️ **Moderate**: Tests completed in 30-60 seconds');
    } else {
      assessments.push('🐌 **Slow**: Tests took >60 seconds');
    }
  }
  
  if (summary.screenshots.length > 0) {
    assessments.push(`📸 **Visual Coverage**: ${summary.screenshots.length} screenshots captured`);
  }
  
  if (summary.videos.length > 0) {
    assessments.push(`🎥 **Video Evidence**: ${summary.videos.length} test videos recorded`);
  }
  
  return assessments.length > 0 ? assessments.join('\n') : '⚠️ No quality assessment available';
}

module.exports = globalTeardown;