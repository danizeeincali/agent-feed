/**
 * Global teardown for SPARC Tailwind validation tests
 */

const fs = require('fs');
const path = require('path');

async function globalTeardown() {
  console.log('🧹 Running SPARC Tailwind CSS validation teardown...');

  try {
    // Update test metadata with completion time
    const metadataPath = path.join(process.cwd(), 'tests/sparc-tailwind-validation/test-metadata.json');

    if (fs.existsSync(metadataPath)) {
      const metadata = JSON.parse(fs.readFileSync(metadataPath, 'utf8'));
      metadata.endTime = new Date().toISOString();
      metadata.duration = new Date(metadata.endTime) - new Date(metadata.startTime);

      fs.writeFileSync(metadataPath, JSON.stringify(metadata, null, 2));
    }

    // Generate summary report
    const summaryPath = path.join(process.cwd(), 'tests/sparc-tailwind-validation/test-summary.md');
    const summary = `# SPARC Tailwind CSS Validation Summary

## Test Execution Completed

- **End Time**: ${new Date().toISOString()}
- **Test Suite**: SPARC Tailwind CSS Validation
- **Status**: Completed

## Artifacts Generated

- ✅ CSS Compilation Tests
- ✅ Runtime Validation Tests
- ✅ Visual Regression Tests
- ✅ Test Reports and Screenshots

## Next Steps

1. Review test results in \`playwright-report/index.html\`
2. Check visual regression screenshots
3. Analyze any failures in test results
4. Update baseline screenshots if needed

---
*Generated by SPARC Tailwind CSS Validation Suite*
`;

    fs.writeFileSync(summaryPath, summary);

    console.log('✅ Teardown completed successfully');
    console.log('📊 Test artifacts saved in tests/sparc-tailwind-validation/');

  } catch (error) {
    console.error('❌ Error during teardown:', error);
  }
}

module.exports = globalTeardown;